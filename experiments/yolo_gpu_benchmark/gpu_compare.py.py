import os, time, cv2
import pandas as pd
from ultralytics import YOLO

NUM_FRAMES = 150
VIDEO_PATH = "test_video.mp4"
SNAPSHOT_FRAME = 50
DEVICE = "cuda:0"   # 强制用GPU

MODELS = [
    ("yolov8n-face.pt",  "YOLOv8"),
    ("yolov11n-face.pt", "YOLOv11"),
    ("yolov12n-face.pt", "YOLOv12"),
]


def run_test(model_path, model_name, frames):
    print(f"\n========== 测试 {model_name} (GPU) ==========")
    model = YOLO(model_path)

    # 热身 (前几帧不计入,GPU首次推理特别慢,要给它"开机时间")
    for _ in range(5):
        _ = model(frames[0], verbose=False, device=DEVICE)

    inference_times = []
    face_counts = []
    snapshot = None

    for i, frame in enumerate(frames):
        torch.cuda.synchronize()
        start = time.perf_counter()
        results = model(frame, verbose=False, device=DEVICE)[0]
        torch.cuda.synchronize()
        end = time.perf_counter()

        infer_ms = (end - start) * 1000
        inference_times.append(infer_ms)
        face_counts.append(len(results.boxes))

        if i == SNAPSHOT_FRAME:
            snapshot = frame.copy()
            for box in results.boxes:
                x1, y1, x2, y2 = map(int, box.xyxy[0])
                conf = float(box.conf[0])
                cv2.rectangle(snapshot, (x1, y1), (x2, y2), (0, 255, 0), 2)
                cv2.putText(snapshot, f"{model_name} {conf:.2f}",
                            (x1, y1 - 10), cv2.FONT_HERSHEY_SIMPLEX,
                            0.7, (0, 255, 0), 2)

        if (i + 1) % 25 == 0:
            print(f"  已处理 {i+1}/{len(frames)} 帧, 当前帧推理: {infer_ms:.2f}ms")

    return inference_times, face_counts, snapshot


def collect_frames():
    print(f"读取视频: {VIDEO_PATH}")
    cap = cv2.VideoCapture(VIDEO_PATH)
    frames = []
    while len(frames) < NUM_FRAMES:
        ret, frame = cap.read()
        if not ret: break
        frames.append(frame)
    cap.release()
    print(f"共采集 {len(frames)} 帧")
    return frames


def stats(times, faces):
    return {
        "Avg Inference (ms)": round(sum(times) / len(times), 2),
        "Min Inference (ms)": round(min(times), 2),
        "Max Inference (ms)": round(max(times), 2),
        "Avg FPS":            round(1000 / (sum(times) / len(times)), 2),
        "Frames with face":   sum(1 for f in faces if f > 0),
        "Detection rate (%)": round(100 * sum(1 for f in faces if f > 0) / len(faces), 2),
    }


frames = collect_frames()
summary_data = {}

for model_path, model_name in MODELS:
    if not os.path.exists(model_path):
        print(f"⚠️ 找不到 {model_path}, 跳过")
        continue
    times, faces, snap = run_test(model_path, model_name, frames)

    pd.DataFrame({
        "frame": list(range(len(times))),
        "inference_ms": times,
        "faces_detected": faces,
    }).to_csv(f"results_{model_name.lower()}_gpu.csv", index=False)

    if snap is not None:
        out_name = f"compare_v{model_name.replace('YOLOv','')}_gpu.jpg"
        cv2.imwrite(out_name, snap)
        print(f"已保存: {out_name}")

    summary_data[f"{model_name}n-face"] = stats(times, faces)

summary = pd.DataFrame(summary_data)
summary.to_csv("summary_gpu.csv")
print("\n========== GPU 汇总结果 ==========")
print(summary)

